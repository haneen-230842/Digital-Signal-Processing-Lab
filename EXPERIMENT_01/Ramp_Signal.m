clear all;
close all;
n1=input("entr the starting point:");
n2=input("Enter end point:");
sample_points=-n1:1:n2;
points=0:1:n2;
amplitude=[zeros(1,n1), points];
subplot(121);
plot(sample_points,amplitude);
xlabel("sample points");
ylabel("amplitude");
title("Ramp");
grid on;
subplot(122);
stem(sample_points,amplitude);
xlabel("sample points");
ylabel("amplitude");
title("Ramp"); 
grid on;